// ===========================

let currentMemory = "";
let currentPhoto = 0;

// ===========================

function openMemory(name){

currentMemory = name;

currentPhoto = 0;

document.getElementById("galleryPage").style.display="none";

document.querySelector("header").style.display="none";

document.getElementById("memoryPage").style.display="block";

document.getElementById("memoryTitle").innerHTML=memories[name].title;

document.getElementById("memoryMessage").innerHTML=memories[name].message;

showPhoto();

}

// ===========================

function showPhoto(){

    const item = memories[currentMemory].media[currentPhoto];

    const img = document.getElementById("slideImage");
    const video = document.getElementById("slideVideo");
    const source = document.getElementById("videoSource");

    if(item.type === "image"){

        video.pause();
        video.style.display = "none";

        img.style.display = "block";
        img.src = item.src;

    }else{

        img.style.display = "none";

        video.style.display = "block";

        source.src = item.src;

        video.load();
        video.play();

    }

}

// ===========================

function nextPhoto(){

if(currentPhoto <
memories[currentMemory].media.length-1){

currentPhoto++;

}else{

currentPhoto=0;

}

showPhoto();

}

// ===========================

function previousPhoto(){

if(currentPhoto>0){

currentPhoto--;

}else{

currentPhoto=
memories[currentMemory].media.length-1;

}

showPhoto();

}

// ===========================

function closeMemory(){

document.getElementById("memoryPage").style.display="none";

document.getElementById("galleryPage").style.display="block";

document.querySelector("header").style.display="block";

}

// ===========================
// KEYBOARD SUPPORT
// ===========================

document.addEventListener("keydown",function(e){

if(document.getElementById("memoryPage").style.display=="block"){

if(e.key==="ArrowRight"){

nextPhoto();

}

if(e.key==="ArrowLeft"){

previousPhoto();

}

if(e.key==="Escape"){

closeMemory();

}

}

});

